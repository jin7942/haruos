import { NavLink } from 'react-router-dom';
import { useAuth } from '../../hooks/useAuth';
import { logout as logoutApi } from '../../api/auth.api';

const navItems = [
  { to: '/dashboard', label: '대시보드', icon: DashboardIcon },
  { to: '/chat', label: '대화', icon: ChatIcon },
  { to: '/projects', label: '프로젝트', icon: ProjectIcon },
  { to: '/schedules', label: '일정', icon: ScheduleIcon },
  { to: '/documents', label: '문서', icon: DocumentIcon },
  { to: '/knowledge', label: '지식', icon: KnowledgeIcon },
  { to: '/files', label: '파일', icon: FileIcon },
  { to: '/batch', label: '배치', icon: BatchIcon },
] as const;

interface SidebarProps {
  isOpen: boolean;
  onClose: () => void;
}

/** 사이드바 네비게이션. 모바일에서는 오버레이로 표시. */
export function Sidebar({ isOpen, onClose }: SidebarProps) {
  const { user, logout } = useAuth();

  async function handleLogout() {
    try {
      await logoutApi();
    } catch {
      // 로그아웃 API 실패해도 로컬 상태는 제거
    }
    logout();
  }

  return (
    <>
      {/* 모바일 오버레이 */}
      {isOpen && (
        <div
          className="fixed inset-0 bg-black/30 z-40 lg:hidden"
          onClick={onClose}
        />
      )}

      <aside
        className={`fixed inset-y-0 left-0 z-50 w-64 bg-white border-r border-gray-200 flex flex-col transition-transform lg:translate-x-0 lg:static lg:z-auto ${
          isOpen ? 'translate-x-0' : '-translate-x-full'
        }`}
      >
        {/* 로고 */}
        <div className="px-6 py-5 border-b border-gray-100">
          <h1 className="text-xl font-bold text-gray-900">HaruOS</h1>
        </div>

        {/* 네비게이션 */}
        <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
          {navItems.map(({ to, label, icon: Icon }) => (
            <NavLink
              key={to}
              to={to}
              onClick={onClose}
              className={({ isActive }) =>
                `flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
                  isActive
                    ? 'bg-blue-50 text-blue-700'
                    : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900'
                }`
              }
            >
              <Icon />
              {label}
            </NavLink>
          ))}
        </nav>

        {/* 사용자 정보 + 로그아웃 */}
        <div className="px-4 py-3 border-t border-gray-100">
          <div className="flex items-center justify-between">
            <div className="min-w-0">
              <p className="text-sm font-medium text-gray-900 truncate">{user?.name}</p>
              <p className="text-xs text-gray-500 truncate">{user?.email}</p>
            </div>
            <button
              onClick={handleLogout}
              className="p-1.5 text-gray-400 hover:text-gray-600 rounded"
              title="로그아웃"
            >
              <LogoutIcon />
            </button>
          </div>
        </div>
      </aside>
    </>
  );
}

